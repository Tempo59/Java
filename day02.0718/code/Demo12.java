class Demo12 
{
	public static void main(String[] args) 
	{
		int a = 3;
		System.out.println(a);//3
		a += 2;//a = a + 2;
		System.out.println(a);//5
		a -= 3;//a = a - 3;
		System.out.println(a);//2
		a *= 2;//a = a * 2;
		System.out.println(a);//4
		a /= 2;//a = a / 2;
		System.out.println(a);//2
		a %= 3;//a = a % 3;
		System.out.println(a);//2
	}
}
